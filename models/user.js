import mongoose from 'mongoose'
const userSchema = new mongoose.Schema({
    firstName:{
        type:String,
        required:true
    },
    lastName:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    
})




const USER=mongoose.model("GRAPHQLUSER",userSchema)

export default USER